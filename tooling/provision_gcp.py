#!/usr/bin/env python3
"""Provision (or tear down) the GCP benchmark fleet.

Thin Python wrapper around the legacy `setup_infra.sh` so the
release-suite tooling has a single Python entry point for fleet
lifecycle. The actual gcloud commands still live in the shell
script — this module just exposes them with a Pythonic argv,
captures + prints reserved external IPs after `--create`, and
emits the env-var snippet the rest of the suite expects.

Usage:
  provision_gcp.py --create                 # create relay + 4 clients
  provision_gcp.py --create --attacker      # also create a 5th VM
  provision_gcp.py --status                 # list current fleet
  provision_gcp.py --delete                 # delete all bench-* VMs
  provision_gcp.py --print-env [--tag X]    # emit
                                             # `export HD_BENCH_GCP_*=...`
                                             # snippet from the live fleet

Out of scope:
  - Reserving static external IPs (do this once per project; the
    suite's `lib/ssh.py` defaults assume reserved addresses).
  - Creating the VPC / subnet / firewall (one-shot, done by
    `setup_infra.sh --create` on first run).

Cost: ~$0.16/h relay + ~$1.30/h aggregate clients @ c4-highcpu in
europe-west4 (Apr 2026 list price). Rough — check `gcloud
compute pricing` for current rates.
"""

import argparse
import os
import shutil
import subprocess
import sys


SETUP_INFRA_SH = os.path.join(
    os.path.dirname(os.path.abspath(__file__)),
    "setup_infra.sh")


def _run_infra(action):
  """Invoke setup_infra.sh with the given action flag."""
  if not os.path.exists(SETUP_INFRA_SH):
    print(f"missing helper: {SETUP_INFRA_SH}", file=sys.stderr)
    return 2
  if not shutil.which("gcloud"):
    print("gcloud CLI not on PATH", file=sys.stderr)
    return 2
  rc = subprocess.call(["bash", SETUP_INFRA_SH, action])
  return rc


def _list_fleet():
  """Return [(name, status, internal_ip, external_ip)] for bench-* VMs."""
  cmd = ["gcloud", "compute", "instances", "list",
         "--filter=name~bench",
         "--format=csv[no-heading](name,status,"
         "networkInterfaces[0].networkIP,"
         "networkInterfaces[0].accessConfigs[0].natIP)"]
  out = subprocess.run(
      cmd, capture_output=True, text=True, check=False)
  rows = []
  for line in out.stdout.splitlines():
    parts = [p.strip() for p in line.split(",")]
    if len(parts) >= 4:
      rows.append(tuple(parts[:4]))
  return rows


def _print_env(tag):
  """Emit a shell snippet exporting HD_BENCH_GCP_* from the live fleet.

  Pipe into a shell or `eval $(provision_gcp.py --print-env)` so
  the rest of the suite picks up the current ephemeral IPs without
  hand-editing `lib/ssh.py`.
  """
  rows = _list_fleet()
  relay = next((r for r in rows
                if r[0].startswith("bench-relay")), None)
  clients = sorted(
      [r for r in rows if r[0].startswith("bench-client")],
      key=lambda r: r[0])
  attacker = next((r for r in rows
                   if r[0].startswith("bench-attacker")), None)
  if not relay or not clients:
    print("# fleet not fully provisioned — relay or clients missing",
          file=sys.stderr)
    return 1
  print(f"# generated by provision_gcp.py --print-env "
        f"(tag={tag or 'live'})")
  print(f"export HD_BENCH_GCP_RELAY={relay[3]}")
  print(f"export HD_BENCH_GCP_RELAY_INTERNAL={relay[2]}")
  print("export HD_BENCH_GCP_CLIENTS={}".format(
      ",".join(c[3] for c in clients)))
  if attacker:
    print(f"export HD_BENCH_GCP_ATTACKER={attacker[3]}")
  return 0


def main(argv=None):
  """Argparse + dispatch."""
  p = argparse.ArgumentParser(
      description="Provision/teardown the GCP benchmark fleet.")
  group = p.add_mutually_exclusive_group(required=True)
  group.add_argument("--create", action="store_true")
  group.add_argument("--delete", action="store_true")
  group.add_argument("--status", action="store_true")
  group.add_argument("--print-env", action="store_true",
                     help="emit shell exports for HD_BENCH_GCP_* "
                     "from the live fleet")
  p.add_argument("--attacker", action="store_true",
                 help="(--create only) also provision a 5th VM "
                 "for T1 hardening rows; not yet wired into "
                 "setup_infra.sh, prints an instruction")
  p.add_argument("--tag", default=None,
                 help="(--print-env only) tag to label the snippet")
  args = p.parse_args(argv)
  if args.create:
    rc = _run_infra("--create")
    if rc == 0:
      print("# bench fleet creation finished — verify with: "
            "provision_gcp.py --status",
            file=sys.stderr)
    if args.attacker:
      print("# --attacker flag noted; setup_infra.sh doesn't "
            "yet provision a 5th VM. Provision it manually and "
            "set HD_BENCH_GCP_ATTACKER in the env.",
            file=sys.stderr)
    return rc
  if args.delete:
    return _run_infra("--delete")
  if args.status:
    return _run_infra("--status")
  if args.print_env:
    return _print_env(args.tag)
  return 0


if __name__ == "__main__":
  sys.exit(main())
